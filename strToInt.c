{
	return (isatty(STDIN_FILENO) && info->readfd <= 2);
}

/**
 *_isalpha - checks for alphabetic character in a character
 *@c: character to be checked
*
 *Return: 1 if true, 0 otherwise
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

/**
 * is_delim - checks for a delimeter character
 * @c: character to be checked
 * @delim: string of delimeter
*
 * Return: 1 is success, 0 fail
 */

int is_delim(char c, char *delim)
{
	while (*delim)
		if (*delim++ == c)
			return (1);
	return (0);
}

/**
 *_atoi - converts string to ingeter
 *@s: string to convert
*
 *Return: 0 if the string is non numeric, converted number otherwise
 */

int _atoi(char *s)
{
	int i, sign = 1, flag = 0, output;
	unsigned int result = 0;

	for (i = 0;  s[i] != '\0' && flag != 2; i++)
	{
		if (s[i] == '-')
			sign *= -1;

		if (s[i] >= '0' && s[i] <= '9')
		{
			flag = 1;
			result *= 10;
			result += (s[i] - '0');
		}
		else if (flag == 1)
			flag = 2;
	}

	if (sign == -1)
		output = -result;
	else
		output = result;

	return (output);
}

